def clean_schema_for_parse_8(schema: Dict[str, Any]) -> Dict[str, Any]:
    """
    Clean schema for Parse Server 8.x compatibility.

    Parse Server 8.x has stricter validation:
    - Built-in fields (ACL, objectId, createdAt, updatedAt) are automatically managed
    - Cannot explicitly define or update these fields
    - Cannot modify existing indexes
    - MongoDB-specific indexes (DocumentDBDefaultIndex) should be removed
    """
    schema = copy.deepcopy(schema)

    # Remove built-in fields that Parse Server manages automatically
    builtin_fields = ['ACL', 'objectId', 'createdAt', 'updatedAt']
    if 'fields' in schema:
        for field in builtin_fields:
            schema['fields'].pop(field, None)

    # Remove DocumentDBDefaultIndex references (MongoDB-specific)
    if 'indexes' in schema:
        schema['indexes'] = {
            k: v for k, v in schema['indexes'].items()
            if 'DocumentDBDefaultIndex' not in str(v)
        }
        # Remove empty indexes dict
        if not schema['indexes']:
            del schema['indexes']

    # Remove addField from classLevelPermissions if it's empty (Parse 8 is stricter)
    if 'classLevelPermissions' in schema:
        if 'addField' in schema['classLevelPermissions'] and not schema['classLevelPermissions']['addField']:
            del schema['classLevelPermissions']['addField']

    return schema


def create_or_update_schema(parse_url: str, app_id: str, master_key: str, schema: Dict[str, Any]) -> bool:
    """
    Create or update a Parse Server schema via REST API.
    Compatible with Parse Server 8.x.
    """
    class_name = schema["className"]

    # Clean schema for Parse 8.x compatibility
    schema = clean_schema_for_parse_8(schema)

    headers = {
        "X-Parse-Application-Id": app_id,
        "X-Parse-Master-Key": master_key,
        "Content-Type": "application/json"
    }

    # Check if schema already exists
    check_url = f"{parse_url}/schemas/{class_name}"
    response = requests.get(check_url, headers=headers)

    if response.status_code == 200:
        # Schema exists - skip update for now (Parse 8 is strict about index updates)
        print(f"✓ Schema '{class_name}' already exists (skipping update to avoid index conflicts)")
        return True
    else:
        # Schema doesn't exist, create it
        print(f"Creating schema '{class_name}'...")
        response = requests.post(f"{parse_url}/schemas", headers=headers, json=schema)

    if response.status_code in [200, 201]:
        print(f"✓ Schema '{class_name}' created successfully")
        return True
    else:
        error_data = response.json() if response.text else {}
        error_code = error_data.get('code')
        error_msg = error_data.get('error', response.text)

        # Ignore certain non-critical errors for built-in classes
        if error_code in [102, 136] and class_name.startswith('_'):
            print(f"⚠ Schema '{class_name}' - non-critical error (built-in class): {error_msg}")
            return True

        print(f"✗ Failed to create schema '{class_name}': {response.status_code}")
        print(f"  Response: {error_msg}")
        return False


